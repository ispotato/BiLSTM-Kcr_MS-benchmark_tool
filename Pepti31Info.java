package evidence_file_tools;

public class Pepti31Info {
    private String peptide;
    private String proteins;
    private String peptide31Samp;

    public String getPeptide() {
        return peptide;
    }

    public void setPeptide(String peptide) {
        this.peptide = peptide;
    }

    public String getProteins() {
        return proteins;
    }

    public void setProteins(String proteins) {
        this.proteins = proteins;
    }

    public String getPeptide31Samp() {
        return peptide31Samp;
    }

    public void setPeptide31Samp(String peptide31Samp) {
        this.peptide31Samp = peptide31Samp;
    }
}
